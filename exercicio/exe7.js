console.log(0 || "Hello");
//hello : retorna o primeiro valor verdadeiro
console.log("" && "World");
// falso: retorna o primeiro valor falso, retornando true apenas se todas as condições forem verdadeiras.
console.log(null ?? "Default Value");
//default value 
// retornaria null caso não tenha nenhum valor ausente
console.log(undefined ?? 42);
// 42
// undefined caso não tenha nenhum valor definido

